package com.bluejay.server.logic.model;

public class Thread
{
    private int postid;
    private String title;

    public int getPostid()
    {
        return postid;
    }

    public void setPostid(int postid)
    {
        this.postid = postid;
    }

    public String getTitle()
    {
        return title;
    }

    public void setTitle(String title)
    {
        this.title = title;
    }
}
